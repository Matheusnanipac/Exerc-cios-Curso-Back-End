#Codigo para dizer se o numero e positivo negativo ou igual a zero

Num = int(input("Digite um numero"))
if Num > 0:
  print("Seu numero e postivo")
if Num < 0:
  print("Seu numero e negativo")
if Num == 0:
  print("Seu numero e igual a 0")



